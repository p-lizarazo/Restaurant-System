    private void BotonBusRestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonBusRestActionPerformed
        // TODO add your handling code here:
        TablaResultadosBusqueda.setRowSorter(new TableRowSorter(TablaResultadosBusqueda.getModel()));
        TablaResultadosBusqueda.setVisible(true);
        if(RadioDist.isSelected()){
            int min=(int)SpinnerDistMin.getValue();
            int max=(int)SpinnerDistMax.getValue();
            TablaResultadosBusqueda.setModel(manejador.buscarPorDistancia(min,max ));
            ConsultarRestaurante.setSelectedIndex(1);
            TablaResultadosBusqueda.setRowSorter(new TableRowSorter(TablaResultadosBusqueda.getModel()));
            
        } else if(RadioPrecio.isSelected()){
            long min=(long)SpinnerPrecioMin.getValue();
            long max=(long)SpinnerPrecioMax.getValue();
            
            TablaResultadosBusqueda.setModel(manejador.buscarPorPrecio(min,max ));
            ConsultarRestaurante.setSelectedIndex(1);
            TablaResultadosBusqueda.setRowSorter(new TableRowSorter(TablaResultadosBusqueda.getModel()));
        
        } else if(RadioTipoComida.isSelected()){
            String tipo=(String)ComboBoxTipoComida.getSelectedItem();
            TablaResultadosBusqueda.setModel(manejador.buscarPorTipo(tipo));
            ConsultarRestaurante.setSelectedIndex(1);
            TablaResultadosBusqueda.setRowSorter(new TableRowSorter(TablaResultadosBusqueda.getModel()));
        } else if (RadioPlato.isSelected()){
            String plato=(String)ComboBoxPLato.getSelectedItem();
            TablaResultadosBusqueda.setModel(manejador.buscarPorPlato(plato));
            ConsultarRestaurante.setSelectedIndex(1);
            TablaResultadosBusqueda.setRowSorter(new TableRowSorter(TablaResultadosBusqueda.getModel()));
            
        } else {
            JOptionPane.showMessageDialog(null, "Escoja un criterio por favor", "Info", JOptionPane.INFORMATION_MESSAGE);
        }
        
        
        
    }//GEN-LAST:event_BotonBusRestActionPerformed

