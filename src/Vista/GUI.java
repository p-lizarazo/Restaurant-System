    private void BotonRegistrarmeRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonRegistrarmeRegistroActionPerformed
        // TODO add your handling code here:
        String usuario,contra,email;
        usuario = TextFieldNombreUsuarioRegistro.getText();
        email=TextFieldEmailRegistro.getText();
        contra=PasswordFildRegistro.getText();
        BigDecimal numeroTelefonico= null;
        BigDecimal numeroOpcional= null;
        if(!FormattedTextFildNumeroCelularRegistro.getText().isEmpty()) 
            numeroTelefonico=new BigDecimal(FormattedTextFildNumeroCelularRegistro.getText());
        if(!FormattedTextFildNumeroOpcionalRegistro.getText().isEmpty()) 
            numeroOpcional=new BigDecimal(FormattedTextFildNumeroOpcionalRegistro.getText());
        String direccion=TextFieldDireccionRegistro.getText();
        String tipo=(String)ComboBoxTipoRegistro.getSelectedItem();
        
        
        try{
            if(tipo.equals("Usuario")){
                        manejador.registrarMobUsuario(usuario, contra, email, numeroTelefonico);
                        JOptionPane.showMessageDialog(null, "Su cuenta fue creado con exito", "Info", JOptionPane.INFORMATION_MESSAGE);
                        LabelContrasenaLogin.setVisible(false);
                        Login.setVisible(true);
            }  else  {
                    if(numeroTelefonico!=null && !direccion.isEmpty()) {
                        manejador.registrarDuenoRestaurante(usuario, contra,email, numeroTelefonico, numeroOpcional , direccion);     
                        JOptionPane.showMessageDialog(null, "Su cuenta fue creado con exito", "Info", JOptionPane.INFORMATION_MESSAGE);
                        LabelContrasenaLogin.setVisible(false);
                        Login.setVisible(true);
                    } else {
                         JOptionPane.showMessageDialog(null,"Ingrese los datos faltantes","Error", JOptionPane.ERROR_MESSAGE);
                    }
                
                    
            }
        } catch(HeadlessException x){
                        JOptionPane.showMessageDialog(null,"Error, verifique sus datos o intente otro nombre de usuario","Error", JOptionPane.ERROR_MESSAGE);
            }
            
        
    }//GEN-LAST:event_BotonRegistrarmeRegistroActionPerformed

